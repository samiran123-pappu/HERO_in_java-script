const a = 5;
const b = 2;

console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);

let n = 5;
n++;
console.log("after ++:", n);
n--;
console.log("after --:", n);

