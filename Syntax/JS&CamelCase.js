// JavaScript and Camel Case
// Historically, programmers have used different ways of joining multiple words into one variable name:

// Hyphens:

// first-name, last-name, master-card, inter-city.

// Hyphens are not allowed in JavaScript. They are reserved for subtractions.

// Underscore:

// first_name, last_name, master_card, inter_city.

// Upper Camel Case (Pascal Case):

// FirstName, LastName, MasterCard, InterCity.


// Lower Camel Case:

// firstName, lastName, masterCard, interCity.

// JavaScript programmers tend to use lower camel case.





// The Lisp family of programming languages (including Scheme and Common Lisp) commonly uses and natively supports hyphens in variable (symbol) names. 
// In most other mainstream programming languages (like Python, JavaScript, C#, etc.), hyphens are generally forbidden in variable names because the parser interprets them as the subtraction operator, leading to syntax errors. 


// Languages that Allow Hyphens in Variables
// Lisp / Scheme: Hyphens are a standard and preferred convention for naming identifiers (symbols) in Lisp-family languages, often used instead of snake_case or camelCase. For example, a variable might be named user-name or total-volume.

// HCL Technologies Software Products (e.g., ZIEWeb): In the macro language for some HCL products, variable names are explicitly stated as being allowed to contain hyphens, in addition to alphanumeric characters and underscores.

// Perl 6 (Raku): This language allows hyphens in variable names and uses a specific parsing rule (the hyphen must be followed by an alphabetic character) to disambiguate them from the subtraction operator. 




// Languages that Do Not Allow Hyphens in Variables 
// Most general-purpose languages reserve the hyphen for the minus/subtraction operator. Examples include: 
// Python
// JavaScript
// C#
// Java
// C/C++
// PHP (in general variable names) 
